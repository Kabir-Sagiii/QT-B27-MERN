var wish = "gaaaaaaaaaaa";

switch (wish) {
  case "gm":
    console.log("Good Morning");

  case "ga":
    console.log("Good Afternoon");

  case "ge":
    console.log("Good Evening");
    break;

  case "gn":
    console.log("Good Night");
    break;

  default:
    console.log("Invalid wish");
}
